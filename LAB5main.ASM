
	.ORIG X3000
		
		LD	R6, STACKPOINTER	
		LD	R0, ISRIDENTIFY	;finding the location of isr
		STI	R0, INTERRUPTVECTOR	;storing in keyboard interrupt vector
		LD	R0, INTERRUPTENABLE	; enabling interrupt method
		STI 	R0, KBSR	;enabling in the kbsr
	
NEWLINE		LEA	R0, WAITING
		PUTS
		LD	R0, ASCIIENTER
		OUT

		LD	R1, DELAY
DELAYAGAIN	ADD	R1, R1, #-1
		BRN	DELAYAGAIN
		
		
		BR	NEWLINE


		HALT


STACKPOINTER	.FILL X3000
ISRIDENTIFY	.FILL X1700
INTERRUPTVECTOR	.FILL X0180
INTERRUPTENABLE	.FILL X4000
KBSR		.FILL XFE00
WAITING		.STRINGZ "Waiting for interrupt ..."
DELAY		.FILL XFFFF
ASCIIENTER	.FILL X0A
		.END